<script setup lang="ts">
import { SupportedExchange } from '@/types/exchanges';
import { Section } from '@/types/status';
import { Routes } from '@/router/routes';

const { shouldShowLoadingScreen, isLoading } = useStatusStore();
const { load, $reset } = useKrakenStakingStore();

const { connectedExchanges } = storeToRefs(useExchangesStore());
const isKrakenConnected = computed(() => {
  const exchanges = get(connectedExchanges);
  return exchanges.some(
    ({ location }) => location === SupportedExchange.KRAKEN
  );
});

onMounted(async () => {
  if (get(isKrakenConnected)) {
    await load(false);
  }
});

onUnmounted(() => {
  $reset();
});

watch(isKrakenConnected, async isKrakenConnected => {
  if (isKrakenConnected) {
    await load(false);
  }
});

const loading = shouldShowLoadingScreen(Section.STAKING_KRAKEN);
const refreshing = isLoading(Section.STAKING_KRAKEN);

const { t } = useI18n();

const refresh = () => load(true);
</script>

<template>
  <div>
    <full-size-content v-if="!isKrakenConnected">
      <v-row align="center" justify="center">
        <v-col>
          <v-row align="center" justify="center">
            <v-col cols="auto">
              <span class="font-weight-bold text-h5">
                {{ t('kraken_page.page.title') }}
              </span>
            </v-col>
          </v-row>
          <v-row justify="center" class="mt-md-12 mt-4">
            <v-col cols="auto" class="mx-4">
              <internal-link :to="Routes.API_KEYS_EXCHANGES">
                <v-img
                  width="64px"
                  contain
                  src="/assets/images/protocols/kraken.svg"
                />
              </internal-link>
            </v-col>
          </v-row>

          <v-row class="mt-md-10 mt-2" justify="center">
            <v-col cols="auto">
              <div
                class="font-weight-light text-h6"
                :class="$style.description"
              >
                <i18n path="kraken_page.page.description">
                  <template #link>
                    <internal-link :to="Routes.API_KEYS_EXCHANGES">
                      {{ t('kraken_page.page.api_key') }}
                    </internal-link>
                  </template>
                </i18n>
              </div>
            </v-col>
          </v-row>
        </v-col>
      </v-row>
    </full-size-content>
    <progress-screen v-else-if="loading">
      <template #message>
        {{ t('kraken_page.loading') }}
      </template>
    </progress-screen>
    <div v-else>
      <v-row justify="end">
        <v-col cols="auto">
          <refresh-button
            :tooltip="t('kraken_staking_events.refresh_tooltip')"
            :loading="refreshing"
            @refresh="refresh()"
          />
        </v-col>
      </v-row>
      <kraken-staking />
    </div>
  </div>
</template>

<style lang="scss" module>
.description {
  text-align: center;
  max-width: 600px;
}
</style>
