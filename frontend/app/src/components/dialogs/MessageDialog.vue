<script setup lang="ts">
const props = withDefaults(
  defineProps<{
    title: string;
    message: string;
    success?: boolean;
  }>(),
  {
    success: false
  }
);

const emit = defineEmits<{
  (e: 'dismiss'): void;
}>();

const { message, success } = toRefs(props);
const visible = ref<boolean>(false);

watch(message, message => {
  set(visible, message.length > 0);
});

const icon = computed<string>(() =>
  get(success) ? 'mdi-check-circle ' : 'mdi-alert-circle'
);

const dismiss = () => emit('dismiss');

const { t } = useI18n();
</script>

<template>
  <div>
    <VDialog
      :value="visible"
      max-width="500"
      class="message-dialog"
      persistent
      @close="dismiss()"
      @keydown.esc="dismiss()"
      @keydown.enter="dismiss()"
    >
      <VCard>
        <VCardTitle
          :class="{ 'green--text': success, 'red--text': !success }"
          class="text-h5 message-dialog__title"
        >
          {{ title }}
        </VCardTitle>
        <VRow align="center" class="mx-0 message-dialog__body">
          <VCol cols="1">
            <VIcon
              size="40"
              class="dialog-icon"
              :class="{ 'green--text': success, 'red--text': !success }"
            >
              {{ icon }}
            </VIcon>
          </VCol>
          <VCol cols="11">
            <VCardText class="message-dialog__message">
              {{ message }}
            </VCardText>
          </VCol>
        </VRow>

        <VCardActions>
          <VSpacer />
          <RuiButton
            :color="success ? 'success' : 'error'"
            variant="text"
            class="message-dialog__buttons__confirm px-4"
            @click="dismiss()"
          >
            {{ t('common.actions.ok') }}
          </RuiButton>
        </VCardActions>
      </VCard>
    </VDialog>
  </div>
</template>

<style scoped lang="scss">
