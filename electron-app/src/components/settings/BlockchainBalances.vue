<template>
  <v-row class="blockchain-balances">
    <v-col>
      <v-card>
        <v-card-title>Blockchain Balances</v-card-title>
        <v-card-text>
          <v-select
            v-model="selected"
            class="blockchain-balances__chain"
            :items="items"
            label="Choose blockchain"
            :disabled="accountOperation"
          ></v-select>
          <v-text-field
            v-model="accountAddress"
            class="blockchain-balances__address"
            label="Account"
            :disabled="accountOperation"
          ></v-text-field>

          <div class="blockchain-balances--progress">
            <v-progress-linear
              v-if="accountOperation"
              indeterminate
            ></v-progress-linear>
          </div>

          <v-btn
            class="blockchain-balances__buttons__add"
            depressed
            color="primary"
            type="submit"
            :disabled="!accountAddress || accountOperation"
            @click="addAccount()"
          >
            Add
          </v-btn>

          <token-track></token-track>
          <v-divider></v-divider>
          <asset-balances
            title="Blockchain Balances per Asset"
            :balances="totals"
          >
          </asset-balances>
          <v-divider></v-divider>
          <account-balances
            title="ETH per account balances"
            blockchain="ETH"
            :balances="ethAccounts"
          ></account-balances>
          <v-divider></v-divider>
          <account-balances
            title="BTC per account balances"
            blockchain="BTC"
            :balances="btcAccounts"
          ></account-balances>
        </v-card-text>
      </v-card>
    </v-col>
  </v-row>
</template>

<script lang="ts">
import { Component, Vue } from 'vue-property-decorator';
import MessageDialog from '@/components/dialogs/MessageDialog.vue';
import TokenTrack from '@/components/settings/TokenTrack.vue';
import { AccountBalance } from '@/model/blockchain-balances';
import { createNamespacedHelpers } from 'vuex';
import AccountBalances from '@/components/settings/AccountBalances.vue';
import AssetBalances from '@/components/settings/AssetBalances.vue';
import { notify } from '@/store/notifications/utils';
import { Blockchain, Severity, SupportedBlockchains } from '@/typing/types';
import { TaskType } from '@/model/task';

const { mapGetters } = createNamespacedHelpers('balances');
const { mapGetters: mapTaskGetters } = createNamespacedHelpers('tasks');

@Component({
  components: { AccountBalances, AssetBalances, TokenTrack, MessageDialog },
  computed: {
    ...mapGetters(['ethAccounts', 'btcAccounts', 'totals']),
    ...mapTaskGetters(['isTaskRunning'])
  }
})
export default class BlockchainBalances extends Vue {
  selected: Blockchain = 'ETH';
  readonly items: string[] = SupportedBlockchains;
  accountAddress: string = '';

  ethAccounts!: AccountBalance[];
  btcAccounts!: AccountBalance[];
  totals!: AccountBalance[];
  isTaskRunning!: (type: TaskType) => boolean;
  pending: boolean = false;

  get accountOperation(): boolean {
    return (
      this.isTaskRunning(TaskType.ADD_ACCOUNT) ||
      this.isTaskRunning(TaskType.REMOVE_ACCOUNT) ||
      this.pending
    );
  }

  async addAccount() {
    const address = this.accountAddress;
    const blockchain = this.selected;
    this.pending = true;
    try {
      await this.$store.dispatch('balances/addAccount', {
        blockchain,
        address
      });
      this.accountAddress = '';
    } catch (e) {
      notify(
        `Error while adding account: ${e}`,
        'Adding Account',
        Severity.ERROR
      );
    }
    this.pending = false;
  }
}
</script>

<style scoped lang="scss">
.blockchain-balances--progress {
  height: 15px;
}
</style>
